//Pedir para o usuario digitar um número
//conveter em numero inteiro
let numero1 = parseInt(prompt('Digite um número'))
//pedir para o usuario digitar outro numero
//converter em numero inteiro
let numero2 = parseInt(prompt('Digite mais um número'))
//mostrar a subtração concatenando com o texto 'a subtração é..'
alert(numero1 + ' - ' + numero2 + ' a subtração é ' + (numero1 - numero2))
//mostrar a multiplicação concetenando com o texto 'a multiplicação é..'
alert(numero1 + ' x ' + numero2 + ' a multiplicação é ' + (numero1 * numero2))
//mostrar a divisao concetenando com o texto 'a divisão é..'
alert(numero1 + ' / ' + numero2 + ' a divisão é ' + (numero1 / numero2))
//mostrar a soma concetenando com o texto 'a soma é..'
alert(numero1 + ' + ' + numero2 + ' a soma é ' + (numero1 + numero2))