//converter em numero inteiro
let numero1 = parseInt(prompt('Digite um número'))
//pedir para o usuario digitar mais um numero
//converter para numero inteiro
let numero2 = parseInt(prompt('Digite outro número'))
/*
mostrar em um alert, o seguinte texto: o resto da divisão de (n1) por (n2) é:(resultado)
*/
alert(`O resto da divisão de ${numero1 + ' e ' + numero2} é: ${numero1 % numero2}`)
/*
mostrar em um alert o seguinte texto: o numero (n1) elevado a (n2) é:(resultado)
*/
alert(`O número ${numero1 + ' elevado a ' + numero2} é: ${numero1**numero2}`)